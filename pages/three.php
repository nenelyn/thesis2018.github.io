								<p style = "text-align:center;">STEP 1: PROTECT YOURSELF FROM BLOODBORNE PATHOGENS.</p>
								<p> Bloodborne pathogens can threaten your health and wellbeing by causing sickness and disease. If you have a first aid kit, sanitize your hands and put on sterile gloves. If sterile gloves and sanitizer are not available, protect your hands with extra gauze or cotton. Avoid direct contact with the other person's blood. If you do end up making contact, make sure to clean yourself off as soon as possible. Eliminate any remaining sources of contamination.</p><br>

								<span class="image main"><img src="assets/images/pic12.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 2: STOP THE BLEEDING FIRST.</p>
								<p> After you have established that the victim is breathing and has a pulse, your next priority should be to control any bleeding. Control of bleeding is one of the most important things you can do to save a trauma victim. Use direct pressure on a wound before trying any other method of managing bleeding. Read the linked article for more detailed steps you can take.Treat a bullet wound. Bullet wounds are serious and unpredictable. Read on for special considerations when treating someone who has suffered a gunshot wound.</p><br>

								<span class="image main"><img src="assets/images/pic13.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 3:TREAT SHOCK NEXT.</p>
								<p> Shock, often caused a loss of blood flow to the body, frequently follows physical and occasionally psychological trauma. A person in shock will frequently have cool, clammy skin, be agitated or have an altered mental status, and have pale color to the skin around the face and lips. Untreated, shock can be fatal. Anyone who has suffered a severe injury or life-threatening situation is at risk for shock.</p><br>


								<span class="image main"><img src="assets/images/pic14.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 4: PROVIDE FIRST AID FOR BROKEN BONES.</p>
								<p> A broken bone, however common, can be treated with the following steps:<ul>
								<li>Immobilize the area. Make sure that the broken bone doesn't have to move or support any other body parts.</li>
								<li>Numb the pain. Often, this can be done with an ice-pack covered by a towel.</li>
								<li>Make a splint. A bundle of newspapers and sturdy tape will do just the trick. A broken finger, for example, can also use another finger as a stabilizing splint.</li>
								<li>Make a sling, if necessary. Tie a shirt or a pillowcase around a broken arm and then around the shoulder.</li></ul></p><br>

								<span class="image main"><img src="assets/images/pic15.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 5:HELP A CHOCKING VICTIM.</p>
								<p> Choking can cause death or permanent brain damage within minutes. Read this article for ways to help a choking victim. The article addresses helping both children and adult choking victims.One of the ways to help a choking victim is the Heimlich maneuver. The Heimlich maneuver is performed by straddling the victim from behind and bear-hugging them with your hands interlocked above their belly-button but beneath their breastbone. Thrust upward to expel air from the lungs and repeat until you are successful in clearing the object from the windpipe.</p><br>

								<span class="image main"><img src="assets/images/pic16.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 6: LEARN HOW TO TREAT A BURN.</p>
								<p> Treat first- and second-degree burns by immersing or flushing with cool water (no ice). Don't use creams, butter or other ointments, and do not pop blisters. Third degree burns should be covered with a damp cloth. Remove clothing and jewelry from the burn, but do not try to remove charred clothing that is stuck to burns.</p><br>

								<span class="image main"><img src="assets/images/pic17.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 7: LOOK OUT FOR AN CONCUSSION.</p>
								<p> If the victim has suffered a blow to the head, look for signs of concussion. Common symptoms include:<ul>
								<li>Disorientation or memory impairment</li>
								<li>Vertigo</li>
								<li>Nausea</li>
								<li>Lethargy</li></ul></p><br>

