								<h2 class="major">Basic First Aid During <br>Emergency</h2>
								<h3>Caring for an Unconscious Person</h3>
								<span class="image main"><img src="assets/images/pic04.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 1: DETERMINE RESPONSIVENESS.</p>
								<p> If a person is unconscious, try to rouse them by gently tickling their bare hands and feet or by speaking to them. If they do not respond to activity, sound, touch, or other stimulation, determine whether they are breathing.</p><br>

								<span class="image main"><img src="assets/images/pic05.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 2: CHECK FOR BREATHING AND A PULSE.</p>
								<p>If unconscious and unable to be roused, check for breathing: look for a rise in the chest area; listen for the sound of air coming in and out; feel for air using the side of your face. If no signs of breathing are apparent, check for a pulse.</p><br>

								<span class="image main"><img src="assets/images/pic06.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 3: IF THE PERSON REMAINS UNRESPONSIVE, PREP FOR CPR.</p>
								<p>Unless you suspect a spinal injury, carefully roll them onto their back and open their airway.[3] If you suspect a spinal injury, leave the person where they are, provided they are breathing. If the person begins to vomit, move them over to their side to help prevent choking.
								 <ul>
								<li>Keep the head and neck aligned.</li>
								 <li>Carefully roll them onto their back while holding their head.</li>
								 <li>Open the airway by lifting the chin.</li>
								</ul></p><br>

								<span class="image main"><img src="assets/images/pic07.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 4: PERFORM 30 CHEST COMPRESSIONS AND TO RESCUE BREATHS AS PART OF CPR.</p>
								<p>In the center of the chest, just below an imaginary line running between the nipples, put your two hands together and compress the chest down approximately 2 inches (5.1 cm) at a rate of 100 compressions per minute. After 30 compressions, give two rescue breaths and check vitals. If the breaths are blocked, reposition the airway. Make sure the head is tilted slightly back and the tongue is not obstructing it. Continue this cycle of 30 chest compressions and two rescue breaths until someone else relieves you.</p><br>

								<span class="image main"><img src="assets/images/pic08.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 5: REMEMBER YOUR ABs OF CPR.</p>
								<p>The ABCs of CPR refer to the three critical things you need to look for.[3]Check these three things frequently as you give the person first aid CPR.
								 <ul>
								<ul>
								<li>Airway. Does the person have an unobstructed airway?</li>
								<li>Breathing. Is the person breathing?</li>
								<li>Circulation. Does the person show a pulse at major pulse points (wrist, carotid artery, groin)?</li>
								</ul></p><br>

								<span class="image main"><img src="assets/images/pic09.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 6: MAKE SURE THE PERSON IS WARM AS WAIT FOR MEDICAL HELP.</p>
								<p>Drape a towel or a blanket over the person if you have one; if you don't remove some of your own clothing (such as your coat or jacket) and use it as a cover until medical help arrives. However if the person has a heatstroke, do not cover him or keep him warm. Instead try to cool him by fanning him and damping him.</p><br>

								<span class="image main"><img src="assets/images/pic10.jpg" alt="" /></span>
								<p style = "text-align:center;">STEP 7: PAY ATTENTIONS TO A LIST OF DONT'S.</p>
								<p>As you administer first aid, be sure to be aware of these things that you should not do in any case:
								 <ul>
								<ul>
								<li>Do not feed or hydrate an unconscious person. This could cause choking and possible asphyxiation.</li>
								<li>Do not leave the person alone. Unless you absolutely need to signal or call for help, stay with the person at all times.</li>
								<li>Do not prop up an unconscious person's head with a pillow.</li>
								<li>Do not slap or splash with water an unconscious person's face. These are movie gimmicks.</li>
								</ul></p><br>

								

